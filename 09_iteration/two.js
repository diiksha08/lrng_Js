//while 

let index=0;
while(index<=10){
    console.log(`value of index ${index}`);
    index=index+2
}

let myArr=['flash', 'batman', 'spiderman']
let i=0
while(i<myArr.length){
    console.log(`value is ${myArr[i]}`);
    i=i+1;
}

//do-while
let score=1;
do {
    console.log(`score is ${score}`);
    score++
} while (score<=10);